/// write a programme scanf use 
#include<stdio.h>
int main(){
    /// use int
    int number;
    // show screen message 
    printf("Enter the intege number :");
    // use scanf, output value show
    scanf("%d",&number);
    //print int = d 
    printf("%d\n",number);


    /// use float
    float grade ;
    // show screen message 
    printf("Enter the Floating Number :");
    // use scanf , output value show this keyword
    scanf("%f",&grade);
    // out put print , 
    printf("%.2f\n",grade);

    return 0;
}