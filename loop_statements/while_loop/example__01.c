// write a programme while loop
// => While loop syntax
/*
first => variable initilization ;
second => while (conditon)
{
printf();
third => increment and decrement ;
}
*/
#include<stdio.h>
int main()
{
    // variable => Initilization
    int i = 1;
    while (i <= 10) // while loop with conditon
    {
        printf("This Number is 10\n");
        i++; // body use - increment and decrement
    }
    

}